let currentQuestionIndex = 0;
 let score = 0;
 let questionsAnswered = 0;
 let selectedAnswer = null;
 const questionsPerCalculation = 5;
 const results = [];

 const questionElement = document.getElementById('question');
 const optionsElement = document.getElementById('options');
 const explanationElement = document.getElementById('explanation');
 const nextButton = document.getElementById('next');
 const progressElement = document.getElementById('progress');
 const resultElement = document.getElementById('result');

 function displayQuestion() {
  const question = moduleQuestions[currentQuestionIndex];
  questionElement.textContent = question.question;

  let optionsHTML = "";
  question.options.forEach((option, index) => {
  optionsHTML += `<li onclick="selectAnswer(${index})">${option}</li>`;
  });
  optionsElement.innerHTML = optionsHTML;

  explanationElement.textContent = "";
  nextButton.style.display = 'none';
  updateProgress();

  if (selectedAnswer !== null) {
  document.querySelectorAll('#options li')[selectedAnswer].classList.add('selected');
  }
 }

 function selectAnswer(selectedIndex) {
  const options = document.querySelectorAll('#options li');
  options.forEach(option => option.classList.remove('selected'));
  options[selectedIndex].classList.add('selected');
  selectedAnswer = selectedIndex;
  nextButton.style.display = 'block';
 }

 function checkAnswer() {
  if (selectedAnswer === null) {
  alert("Please select an answer before proceeding.");
  return false;
  }

  const question = moduleQuestions[currentQuestionIndex];
  const options = document.querySelectorAll('#options li');
  let isCorrect = false;

  if (question.options[selectedAnswer].charAt(0) === question.correctAnswer) {
  isCorrect = true;
  }

  options.forEach((option, index) => {
  if (question.options[index].charAt(0) === question.correctAnswer) {
  option.classList.add('correct');
  } else if (index === selectedAnswer && !(question.options[index].charAt(0) === question.correctAnswer)) {
  option.classList.add('incorrect');
  }
  });

  if (isCorrect) {
  score++;
  explanationElement.textContent = "Correct! " + question.explanation;
  } else {
  explanationElement.textContent = "Incorrect! " + question.explanation;
  }

  results.push({
  question: question.question,
  selectedAnswer: selectedAnswer,
  correctAnswer: question.correctAnswer,
  isCorrect: isCorrect,
  });

  return true;
 }

 function updateProgress() {
    progressElement.textContent = `Question ${currentQuestionIndex + 1} of ${moduleQuestions.length}`;
 }

 function displayIntermediateResult() {
    let resultText = `Results after ${questionsAnswered} questions:\n`;
    let correctCount = 0;
  for (let i = results.length - questionsPerCalculation; i < results.length; i++) {
       if (results[i].isCorrect) {
       correctCount++;
  }
     resultText += `-${results[i].isCorrect ? ' Correct ' : 'Incorrect'}\n`;
  }
    resultText += `Score: ${correctCount} out of ${questionsPerCalculation}\n`;
    alert(resultText);
 }

 function displayResult() {
      questionElement.style.display = 'none';
      optionsElement.style.display = 'none';
      nextButton.style.display = 'none';
      explanationElement.style.display = 'none';
      progressElement.style.display = 'none';
      resultElement.textContent = `You scored ${score} out of ${moduleQuestions.length}!`;
 }

 nextButton.addEventListener('click', () => {
  if (checkAnswer()) {
     questionsAnswered++;
  if (questionsAnswered % questionsPerCalculation === 0) {
     displayIntermediateResult();
  }

      currentQuestionIndex++;
      selectedAnswer = null;

  if (currentQuestionIndex < moduleQuestions.length) {
      displayQuestion();
  } else {
    displayResult();
  }
  }
 });

 displayQuestion();